--- Собрать payload для ShowDialogue: клиенту уходит только Steps, не весь DialogueData.
function DialogueData.BuildShowPayload(dialogueId: string?, fields: { [string]: any }?)
    fields = fields or {}
    if dialogueId and not fields.Steps then
        fields.Steps = DialogueData.Dialogues[dialogueId]
    end
    if dialogueId then
        fields.DialogueId = dialogueId
    end
    return fields
end

return DialogueData
