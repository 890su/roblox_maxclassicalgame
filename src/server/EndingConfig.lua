-- ═══════════════════════════════════════════════════════════
-- ВЫБОР КОНЦОВКИ ДЛЯ ИГРОКА
-- Вызывается при старте игры в GameManager.
-- Определяет, какую концовку увидит игрок на основе
-- количества уже пройденных прохождений и наличия кода.
--
-- Параметры:
--   endingsCount (number) — сколько раз игрок уже прошёл игру
--   hasCode (boolean)     — ввёл ли игрок секретный код
--
-- Возвращает: таблицу с данными концовки
-- ═══════════════════════════════════════════════════════════
function EndingConfig.GetEndingForPlayer(endingsCount, hasCode)
    -- Перебираем концовки по порядку (1 → 2 → 3)
    for _, ending in ipairs(EndingConfig.Endings) do
        if ending.RequiredEndingsCount == endingsCount then
            -- Концовка 3 требует код — если кода нет, откатываемся к концовке 2
            if ending.RequiresCode and not hasCode then
                return EndingConfig.Endings[2]
            end
            return ending
        end
    end

    -- Если игрок прошёл все 3 концовки — показываем последнюю повторно
    return EndingConfig.Endings[3]
end

return EndingConfig
